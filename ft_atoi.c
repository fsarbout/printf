#include "printf.h"

int		ft_atoi(const char **s)
{
	int		sign;
	long	a;

	sign = 1;
	a = 0;
	while (**s == ' ' || (**s <= 13 && **s >= 9))
		(*s)++;
	if ((**s) == '-')
	{
		sign = -1;
		(*s)++;
	}
	else if (**s == '+')
		(*s)++;
	while ((**s <= '9') && (**s >= '0'))
	{
		if (a < 0)
			return ((sign > 0) ? (-1) : 0);
		a = a * 10 + (**s) - '0';
		(*s)++;
	}
	return ((int)a * sign);
}
