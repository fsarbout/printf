#include "printf.h"

int		ft_strchr_pnt(const char *s)
{
	int c;

	c = '.';
	while (*s != (char)c)
	{
		if (!*s || *s == 'd' || *s == 'i' || *s == 'u'
				|| *s == 'x' || *s == 'X' || *s == 'p' ||
				*s == '%' || *s == 'c' || *s == 's')
		{
			return (0);
		}
		s++;
	}
	g_st.pnt = 1;
	return (1);
}
